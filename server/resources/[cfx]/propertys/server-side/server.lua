-- HOMES:ENTER
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterServerEvent("homes:Enter")
AddEventHandler("homes:Enter",function(homeName)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		TriggerClientEvent("dynamic:closeSystem",source)
		
		if not exports["hud"]:Wanted(user_id) then
			local ownerConsult = vRP.query("propertys/userOwnermissions",{ name = homeName })
			if ownerConsult[1] then
				local consult = vRP.query("propertys/userPermissions",{ name = homeName, user_id = user_id })
				if consult[1] then
					if ownerConsult[1]["tax"] <= os.time() then
						TriggerClientEvent("Notify",source,"amarelo","Taxas da propriedade atrasada.",10000)
						return false
					end

					TriggerClientEvent("Notify",source,"azul",completeTimers(ownerConsult[1]["tax"] - os.time()),1000)
					exports["propertys"]:enterHomes(source,user_id,homeName,false)
				else
					if not homeLock[homeName] then
						TriggerClientEvent("Notify",source,"amarelo","Propriedade trancada.",3000)
					else
						if ownerConsult[1]["tax"] <= os.time() then
							TriggerClientEvent("Notify",source,"amarelo","Taxas da propriedade atrasada.",10000)
							return false
						end

						exports["propertys"]:enterHomes(source,user_id,homeName,false)
					end
				end
			end
		end
	end
end)
