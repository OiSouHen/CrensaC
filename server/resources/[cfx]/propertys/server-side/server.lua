exports("initNewspapers",function(source)
	local homeName = nearestHomes(source)
	if homeName then
		if Newspapers[homeName] == nil then
			Newspapers[homeName] = os.time() + 3600
			return true
		else
			if os.time() >= Newspapers[homeName] then
				Newspapers[homeName] = os.time() + 3600
				return true
			end

			local newspaperTime = parseInt(Newspapers[homeName] - os.time())
			TriggerClientEvent("Notify",source,"azul","Aguarde <b>"..newspaperTime.." segundos</b>.",5000)
		end
	end

	return false
end)
