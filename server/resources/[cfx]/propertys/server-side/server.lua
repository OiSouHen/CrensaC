exports("enterHomes",function(source,user_id,homeName,status)
	local consult = vRP.query("propertys/permissions",{ name = homeName })
	if consult[1] then
		homeInterior[homeName] = consult[1]["interior"]
	else
		if homeInterior[homeName] == nil then
			homeInterior[homeName] = infoInterior[math.random(#infoInterior - 1)]["interior"]
		end
	end

	setNetwork(source,user_id,homeName,status)
end)
