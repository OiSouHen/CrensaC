-- NEARESTHOMES
-----------------------------------------------------------------------------------------------------------------------------------------
function nearestHomes(source)
	local ped = GetPlayerPed(source)
	local coords = GetEntityCoords(ped)

	for k,v in pairs(homes) do
		local distance = #(coords - vec3(v[1],v[2],v[3]))
		if distance <= 1.5 then
			return k
		end
	end

	return false
end
