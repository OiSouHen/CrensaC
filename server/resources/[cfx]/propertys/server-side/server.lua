RegisterServerEvent("homes:invokeSystem")
AddEventHandler("homes:invokeSystem",function(mode)
	local source = source
	local homeName = nearestHomes(source)
	if homeName then
		local user_id = vRP.getUserId(source)
		if user_id and actived[user_id] == nil then
			actived[user_id] = true

			local consult = vRP.query("propertys/userPermissions",{ name = homeName, user_id = user_id })
			if consult[1] then
				if mode == "vender" and consult[1]["owner"] >= 1 then
					TriggerClientEvent("dynamic:closeSystem",source)

					local homesPrice = parseInt(consult[1]["price"] * 0.7)

					if vRP.request(source,"Concluir a venda por <b>$"..parseFormat(homesPrice).."</b> dlares?") then
						local userPermissions = vRP.query("propertys/userPermissions",{ name = homeName, user_id = user_id })
						if userPermissions[1] then
							TriggerClientEvent("Notify",source,"verde","Venda concluda.",5000)
							TriggerEvent("garages:removeGarages",homeName)
							vRP.execute("propertys/selling",{ name = homeName })
							vRP.addBank(user_id,homesPrice,"Private")
							vRP.remSrvdata("wardrobe:"..homeName)
							vRP.remSrvdata("fridge:"..homeName)
							vRP.remSrvdata("vault:"..homeName)
							homeBlips[homeName]["color"] = 2
						end
					end
				elseif mode == "garagem" and consult[1]["owner"] >= 1 then
					TriggerClientEvent("dynamic:closeSystem",source)

					local userPermissions = vRP.query("propertys/userPermissions",{ name = homeName, user_id = user_id })
					if userPermissions[1] then
						TriggerClientEvent("Notify",source,"amarelo","Fique no local onde vai abrir a garagem e pressione a tecla <b>E</b>.",10000)
						vCLIENT.homeGarage(source,homeName)
					end
				elseif mode == "tax" then
					TriggerClientEvent("dynamic:closeSystem",source)

					local ownerConsult = vRP.query("propertys/userOwnermissions",{ name = homeName })
					if ownerConsult[1] then
						local taxPrice = parseInt(ownerConsult[1]["price"] * 0.1)

						if vRP.request(source,"Pagar as taxas por <b>$"..parseFormat(taxPrice).."</b> dlares?") then
							local userPermissions = vRP.query("propertys/userPermissions",{ name = homeName, user_id = user_id })
