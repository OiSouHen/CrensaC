									permList = permList.."<br>"
								end
							end
						end

						TriggerClientEvent("Notify",source,"azul","Lista de permisses da(o): <br>"..permList,20000)
					end
				elseif mode == "tax" then
					TriggerClientEvent("dynamic:closeSystem",source)

					local ownerConsult = vRP.query("propertys/userOwnermissions",{ name = homeName })
					if ownerConsult[1] then
						local taxPrice = parseInt(ownerConsult[1]["price"] * 0.1)

						if vRP.request(source,"Pagar as taxas por <b>$"..parseFormat(taxPrice).."</b> dlares?") then
							local userPermissions = vRP.query("propertys/userPermissions",{ name = homeName, user_id = user_id })
							if userPermissions[1] then
								if vRP.paymentFull(user_id,taxPrice) then
									vRP.execute("propertys/updateTax",{ name = homeName })
									TriggerClientEvent("Notify",source,"verde","Pagamento efetuado.",5000)
								else
									TriggerClientEvent("Notify",source,"vermelho","<b>Dlares</b> insuficientes.",5000)
								end
							end
						end
					end
				elseif mode == "armario" then
					local consultItem = vRP.getInventoryItemAmount(user_id,"diagram")
					if consultItem[1] <= 0 then
						TriggerClientEvent("Notify",source,"amarelo","Necessrio possuir <b>1x Diagram</b>.",5000)
						return
					end
					
					if vRP.request(source,"Aumentar o armrio por <b>$10.000</b> dlares?") then
						local userPermissions = vRP.query("propertys/userPermissions",{ name = homeName, user_id = user_id })
