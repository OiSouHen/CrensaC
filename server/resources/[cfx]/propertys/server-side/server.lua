exports("initNewspapers",function(source)
	local homeName = nearestHomes(source)
	if homeName then
		if Newspapers[homeName] == nil then
			Newspapers[homeName] = os.time() + 3600
			return true
		else
			if os.time() >= Newspapers[homeName] then
				Newspapers[homeName] = os.time() + 3600
				return true
			end
		end
	end

	return false
end)
